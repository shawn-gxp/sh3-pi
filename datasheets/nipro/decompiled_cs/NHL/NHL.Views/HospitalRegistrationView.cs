	private async void HospitalSelectedButtonClicked(object sender, EventArgs e)
	{
		if (string.IsNullOrEmpty(IoC.Get<IUserManager>().IgUser.Name))
		{
			await DisplayAlert("", NOUSERNAME_MESSAGE, "OK");
		}
		else
		{
			StartScanner();
		}
	}

	private async void StartScanner()
	{
		MobileBarcodeScanningOptions options = new MobileBarcodeScanningOptions
		{
			AutoRotate = false,
			TryHarder = true
		};
		scanPage = new ZXingScannerPage(options)
		{
			DefaultOverlayShowFlashButton = true,
			DefaultOverlayTopText = "データ共有先コードを撮影してください",
			DefaultOverlayBottomText = "データ共有先コードを撮影してください"
		};
		scanPage.OnScanResult += OnScanResult;
		await base.Navigation.PushAsync(scanPage);
	}

	private void OnScanResult(Result result)
	{
		if (!(result.ResultPoints[0].Y < 100f) && !string.IsNullOrEmpty(result.Text?.Trim()))
		{
			scanPage.OnScanResult -= OnScanResult;
			scanPage.IsScanning = false;
			Device.BeginInvokeOnMainThread(async delegate
			{
				await base.Navigation.PopAsync();
				IoC.Get<IEventAggregator>().PublishOnUIThread(new HospitalScannedEvent
				{
					Text = result.Text
				});
			});
		}
	}

